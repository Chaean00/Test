package chapter2;

public class BinaryTest {
    public static void main(String[] args) {
        int num = 10; // 10진수
        int num2 = 0B1010; // 2진수
        int num3 = 012; // 8진수
        int num4 = 0XA; // 16진수

        System.out.println(num);
        System.out.println(num2);
        System.out.println(num3);
        System.out.println(num4);
    }
}
